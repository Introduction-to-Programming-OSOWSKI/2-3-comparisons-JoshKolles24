def greaterThan(x, y):
    if x > y:
        return True
    else:
        return False
print(greaterThan(6, 5))

def lessThan(x, y):
    if x < y:
        return True
    else:
        return False
print(lessThan(5, 6))

def equalTo(x, y):
    if x == y:
        return True
    else:
        return False
print(equalTo(5, 5))

def lessOrEqual(x, y):
    if x <= y:
        return True
    else:
        return False
print(lessOrEqual(5, 5))

def greaterOrEqual(x, y):
    if x >= y:
        return True
    else:
        return False
print(greaterOrEqual(5, 5))

